#list type
a = ["apple", "banana", "cherry"]
print(a)
print(type(a))

#['apple', 'banana', 'cherry']
#<class 'list'>

#tipule type
b = tuple(("apple", "banana", "cherry"))
print(b)
print(type(b))

#set type
c = {"apple", "banana", "cherry", True, 1, 2}
print(c)
print(type(c))

#['apple', 'banana', 'cherry']
#<class 'list'>
#('apple', 'banana', 'cherry')
#<class 'tuple'>
#{True, 2, 'cherry', 'apple', 'banana'}
#<class 'set'>